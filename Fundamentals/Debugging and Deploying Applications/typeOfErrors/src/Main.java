// Type of Error in java
// 1. Syntax Error
// 2. Runtime Error
// 3. Logical Error
// 4. Compile Time Error
// 5. Link Time Error


public class Main {
    public static void main(String[] args) {
        String name = "Rahul";
        int age = 20;

        System.out.println(name + " is " + age + " years old");

        // Runtime Error
        System.out.println(name + " is " + age + " years old");

        // Logical Error
        System.out.println(name + " is " + age + " years old");

        // Compile Time Error
        System.out.println(name + " is " + age + " years old");

    }
}